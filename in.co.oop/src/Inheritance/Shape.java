package Inheritance;

public class Shape {
	
	private String color;
	private int borderWidth;
	
	public void setColor(String a) {
		color=a;
	}
	public String getColor() {
		return color;
	}
	public void setBorderWidth(int b) {
		borderWidth=b;
	}
	public int getBorderWidth() {
		return borderWidth;
	}

}
